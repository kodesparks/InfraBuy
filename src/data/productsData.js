// Get all products as a flat array
export const getAllProducts = () => {
  return [
    ...productsData.cement,
    ...productsData.steel,
    ...productsData.concrete
  ];
};

// Get products by category
export const getProductsByCategory = (categoryName) => {
  const categoryKey = categoryName.toLowerCase().replace(' ', '');
  return productsData[categoryKey] || [];
};

// Get filter options by category
export const getFilterOptionsByCategory = (categoryName) => {
  return filterOptions[categoryName] || { types: ['All'], grades: ['All'] };
};
